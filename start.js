let deadImages = [];

const server = Hapi.server({
    port: process.env.PORT || 3000,
    host: '0.0.0.0'
});

function recSwap(data) {
  let src = images[Math.floor(Math.random()*images.length)];
  return {
    ...data,
    image_src: src,
    raw_image_src: src,
  };
}

function componentSwap(data) {
  if (!data.feed || !data.feed.url) {
    return data;
  }
  return {
    ...data,
    feed: {
      ...data.feed,
      url: "http://localhost:3000/recommendations?url=" + encodeURIComponent(data.feed.url),
    },
  };
}

function layoutSwap(data) {
  return {
    ...data,
    components: data.components.map(componentSwap),
  };
}

function recommendations(data) {
  return {
    ...data,
    recommendations: data.recommendations.map(recSwap),
  };
}

function layout(data) {
  return {
    ...data,
    layout: data.layout.map(layoutSwap),
  };
}

function awaitFetch(url) {
  return new Promise((resolve) => {
    fetch(url)
      .then(res => res.json())
      .then(resolve);
  });
}

